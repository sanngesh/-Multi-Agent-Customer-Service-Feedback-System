"""
Agent 3: Quality Verifier
Cross-checks the Feedback Generator's output against the original transcripts
and quality standards using GPT-4o. Implements a feedback loop.
"""

import json
from openai import OpenAI
from pydantic import BaseModel, Field
from typing import List, Literal

class MetricScores(BaseModel):
    correctness: int = Field(..., description="Score out of 25: Did they identify the customer's actual problem accurately?")
    completeness: int = Field(..., description="Score out of 20: Did they provide all necessary information or solutions?")
    clarity: int = Field(..., description="Score out of 15: Were their statements unambiguous and easy to understand?")
    format_style: int = Field(..., description="Score out of 20: Did they adhere to professional banking chat formats and greetings?")
    usefulness: int = Field(..., description="Score out of 20: Was their intervention actually helpful and constructive?")

    @property
    def total(self) -> int:
        return self.correctness + self.completeness + self.clarity + self.format_style + self.usefulness

class Issue(BaseModel):
    item_index: int = Field(..., description="Index of the feedback item that has the issue")
    issue_type: Literal["transcript_accuracy", "explanation_quality", "alternative_quality", "completeness", "tone"]
    description: str = Field(..., description="What is wrong with the generated feedback item")
    suggested_fix: str = Field(..., description="How the feedback generator should fix it")

class MissingStatement(BaseModel):
    statement: str = Field(..., description="The Support statement that should have been flagged but wasn't")
    reason: str = Field(..., description="Why this statement is problematic and needs feedback")

class EvaluationResult(BaseModel):
    llm_feedback_scores: MetricScores = Field(..., description="The 100-point rubric scores evaluating the AI's generated feedback document")
    score_justification: str = Field(..., description="1-2 sentences explaining the point breakdown and primary deductions for the LLM feedback")
    approved: bool = Field(..., description="True if no issues were found AND the total score is high enough. False otherwise.")
    overall_quality: Literal["excellent", "good", "needs_revision"]
    items_reviewed: int
    issues_found: List[Issue] = Field(default_factory=list)
    missing_statements: List[MissingStatement] = Field(default_factory=list)


SYSTEM_PROMPT = """You are a strict quality assurance evaluator for an AI customer service feedback generator at a bank. 
Your job is to read the original chat transcript and evaluate the *Feedback Draft* generated by the AI coach.

**Task 1: Evaluate the AI Feedback Generator's Performance (100 Points)**
You must assign integer scores evaluating HOW GOOD THE GENERATED FEEDBACK IS based on this exact rubric:
- **Correctness (25 pts):** Did the feedback identify the actual problems accurately? Are the explanations correct?
- **Completeness (20 pts):** Did the feedback flag all problematic statements in the transcript? Does every item have an explanation and alternative?
- **Clarity (15 pts):** Are the feedback explanations unambiguous, concise (1-3 sentences), and easy to understand?
- **Format/Style (20 pts):** Do the proposed alternatives sound like a professional banking representative? Do they avoid PII leakage and overpromising?
- **Usefulness (20 pts):** Is the feedback actually constructive and helpful for the representative to learn from?

Deduct points based on the severity and frequency of issues found in the *generated feedback*. Provide a brief 'score_justification'.

**Task 2: Verify the Feedback Document's Rules**
For each feedback item generated by the AI coach, verify ALL of the following strict rules:

1. **Transcript Accuracy**: The "original" statement must appear EXACTLY in the transcript as a "Support:" line. Check character-by-character.
2. **Explanation Quality**: Must be 1–3 sentences. Must use category terminology (tone, empathy, clarity, ownership, etc). Must explain WHY the statement is problematic.
3. **Alternative Quality**: Must be substantively different from the original. Must NOT introduce new PII. Must NOT overpromise. Must be professional.
4. **Tone of Feedback**: Must not contain insulting, sarcastic, or demeaning language about the human colleague.

If any rules are broken, or if the "total" score of the feedback is too low, you must set "approved" to False and detail the exact `issues_found`. Check if any required flagged statements were missed (`missing_statements`).
"""

class EvaluationAgent:
    """LLM-powered agent that validates feedback quality and scores the LLM's performance."""

    def __init__(self, client: OpenAI, model: str = "gpt-4o"):
        self.client = client
        self.model = model

    def verify(self, feedback: dict, transcript_text: str, case_name: str) -> EvaluationResult:
        """
        Verify the quality of generated feedback against the transcript using Pydantic structured outputs.

        Args:
            feedback: Output from FeedbackGenerator with feedback_items.
            transcript_text: Original transcript for cross-reference.
            case_name: Name of the case.

        Returns:
            EvaluationResult pydantic object containing scores, approved status, and issues.
        """
        feedback_json = json.dumps(feedback.get("feedback_items", []), indent=2)

        user_prompt = f"""Verify the following feedback generated by the AI for "{case_name}" against the original transcript.

ORIGINAL TRANSCRIPT:
---
{transcript_text}
---

AI FEEDBACK DRAFT TO VERIFY:
{feedback_json}

Check every item against all verification criteria and assign scores based on the 100-point rubric."""

        response = self.client.beta.chat.completions.parse(
            model=self.model,
            response_format=EvaluationResult,
            messages=[
                {"role": "system", "content": SYSTEM_PROMPT},
                {"role": "user", "content": user_prompt},
            ],
            temperature=0.0,
        )

        return response.choices[0].message.parsed
